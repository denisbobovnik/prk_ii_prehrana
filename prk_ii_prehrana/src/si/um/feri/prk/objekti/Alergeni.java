package si.um.feri.prk.objekti;

import java.util.ArrayList;

public class Alergeni {
	private int id;
	private String ime;

	public Alergeni() {
		super();
	}
	
	public Alergeni(int id, String ime) {
		super();
		this.id = id;
		this.ime = ime;
	}
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getIme() {
		return ime;
	}

	public void setIme(String ime) {
		this.ime = ime;
	}
	
	
	
	
	

}